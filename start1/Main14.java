package start;

public class Main14 {

    public static void main(String[] args) {
        int myNum = 5;                          // integer
        float myFloatNum = 5.99f;               // float
        double myDoubleNum = 5.99d;             // double 
        float f1 = 35e3f;                       // float
        double d1 = 12E4d;                      // double
        char myLetter = 'D';                    // character 
        char myVar1 = 65;                       // character 
        boolean myBool = true;                  // boolean 
        byte myByteNum = 100;                   // byte  
        short myShortNum = 5000;                // short  
        long myLongNum = 15000000000L;          // long  
        String myText = "Hello";                // String   -     Hello      
        System.out.println(myNum);              // 5
        System.out.println(myFloatNum + " " + myDoubleNum);      //  5.99   5.99
        System.out.println(f1 + " " + d1);                       // 35000.0   120000.0
        System.out.println(myLetter + " " + myVar1);             // D A
        System.out.println(myByteNum + " " + myShortNum);        // 100   5000
        System.out.println(myBool + " " + myLongNum);            // true 15000000000
        System.out.println(myText);
    }
}
