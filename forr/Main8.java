package forr;

public class Main8 {

    public static void main(String[] args) {
        int number = 2;
        for (int i = 1; i <= 4; i++) {
            System.out.println(number + " x " + i + " = " + (number * i));
        }
    }
}
// 2 x 1 = 2
// 2 x 2 = 4
// 2 x 3 = 6
// 2 x 4 = 8
